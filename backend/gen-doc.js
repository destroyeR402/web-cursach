// ===== Хелперы =====
const run = (t, o = {}) => new TextRun({ text: String(t == null ? '' : t), font: FONT, size: TS, ...o });

function P(text, opts = {}) {
  const runs = Array.isArray(text) ? text : [run(text, opts.run)];
  return new Paragraph({
    children: runs,
    alignment: opts.align || AlignmentType.JUSTIFIED,
    spacing: { line: 360, after: opts.afterSp ?? 100, ...opts.spacing },
    indent: opts.firstLine === false ? undefined : { firstLine: 720 },
  });
}
const PL = (t, o = {}) => P(t, { ...o, firstLine: false, align: o.align || AlignmentType.LEFT });
const PC = (t, o = {}) => P(t, { ...o, firstLine: false, align: AlignmentType.CENTER });

function H1(text, opts = {}) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_1,
    pageBreakBefore: opts.pageBreak !== false,
    alignment: AlignmentType.LEFT,
    spacing: { before: 240, after: 240, line: 360 },
    children: [new TextRun({ text, font: FONT, size: TS_H1, bold: true, allCaps: true })],
  });
}
function H2(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 240, after: 160, line: 360 },
    children: [new TextRun({ text, font: FONT, size: TS_H2, bold: true })],
  });
}
function H3(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_3,
    spacing: { before: 200, after: 120, line: 360 },
    children: [new TextRun({ text, font: FONT, size: TS_H3, bold: true })],
  });
}

function FigPlaceholder(label, h = 2200) {
  // «рамка» под рисунок с подписью внутри
  return new Table({
    width: { size: CONTENT_W, type: WidthType.DXA },
    columnWidths: [CONTENT_W],
    rows: [new TableRow({
      height: { value: h, rule: 'atLeast' },
      children: [new TableCell({
        borders: BRS,
        width: { size: CONTENT_W, type: WidthType.DXA },
        margins: { top: 600, bottom: 600, left: 200, right: 200 },
        children: [new Paragraph({
          alignment: AlignmentType.CENTER,
          children: [new TextRun({ text: `[ ${label} ]`, font: FONT, size: TS_SM, italics: true, color: '888888' })],
        })],
      })],
    })],
  });
}
function FigCaption(text) {
  return new Paragraph({
    alignment: AlignmentType.CENTER,
    spacing: { before: 60, after: 200, line: 360 },
    children: [new TextRun({ text, font: FONT, size: TS_SM, italics: true })],
  });
}
function TblCaption(text) {
  return new Paragraph({
    alignment: AlignmentType.RIGHT,
    spacing: { before: 160, after: 40, line: 320 },
    children: [new TextRun({ text, font: FONT, size: TS_SM })],
  });
}
function TblTitle(text) {
  return new Paragraph({
    alignment: AlignmentType.CENTER,
    spacing: { after: 160, line: 320 },
    children: [new TextRun({ text, font: FONT, size: TS_SM, bold: true })],
  });
}

const Bul = (t, lvl = 0) => new Paragraph({
  numbering: { reference: 'bullets', level: lvl },
  spacing: { line: 360, after: 40 },
  alignment: AlignmentType.JUSTIFIED,
  children: [run(t)],
});
const Num = (t, lvl = 0) => new Paragraph({
  numbering: { reference: 'numbers', level: lvl },
  spacing: { line: 360, after: 40 },
  alignment: AlignmentType.JUSTIFIED,
  children: [run(t)],
});

function MkTable(headers, rows, colWs) {
  const cw = colWs || (function() {
    const eq = Math.floor(CONTENT_W / headers.length);
    const a = Array(headers.length).fill(eq);
    a[a.length - 1] = CONTENT_W - eq * (headers.length - 1);
    return a;
  })();
  const head = new TableRow({
    tableHeader: true,
    children: headers.map((h, i) => new TableCell({
      borders: BRS,
      width: { size: cw[i], type: WidthType.DXA },
      shading: { fill: 'E7E6E6', type: ShadingType.CLEAR },
      margins: { top: 60, bottom: 60, left: 100, right: 100 },
      children: [new Paragraph({ alignment: AlignmentType.CENTER, children: [new TextRun({ text: h, font: FONT, size: TS_SM, bold: true })] })],
    })),
  });
  const body = rows.map((cells) => new TableRow({
    children: cells.map((c, i) => new TableCell({
      borders: BRS,
      width: { size: cw[i], type: WidthType.DXA },
      margins: { top: 50, bottom: 50, left: 100, right: 100 },
      children: (Array.isArray(c) ? c : [c]).map((line) => new Paragraph({
        alignment: AlignmentType.LEFT,
        spacing: { line: 300, after: 30 },
        children: [new TextRun({ text: String(line == null ? '' : line), font: FONT, size: TS_SM })],
      })),
    })),
  }));
  return new Table({
    width: { size: CONTENT_W, type: WidthType.DXA },
    columnWidths: cw,
    rows: [head, ...body],
  });
}

